public class Array1 {
	public static void main(String[] args) {
		String cats[] = {"terry","kitty","toby","fido"};
		String colors[] = {"brown","white","gray","black"};
		System.out.println("Nama Kucing\tWarna");
		System.out.println(cats[0] + "\t\t" + colors[0]);
		System.out.println(cats[1] + "\t\t" + colors[1]);
		System.out.println(cats[2] + "\t\t" + colors[2]);
		System.out.println(cats[3] + "\t\t" + colors[3]);
	}
}